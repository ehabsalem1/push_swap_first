#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	x;
	char	w;
	char	*q;

	x = 0;
	w = c;
	q = (char *)s;
	while (x < n)
	{
		q[x] = w;
		x++;
	}
	return (s);
}
